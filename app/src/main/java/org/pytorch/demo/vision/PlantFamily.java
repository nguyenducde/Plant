package org.pytorch.demo.vision;

public class PlantFamily {
    public int background;
    public String name;
    public String description;
    public String numGenus;

    public PlantFamily(int background, String name, String description, String numGenus) {
        this.background = background;
        this.name = name;
        this.description = description;
        this.numGenus = numGenus;
    }
}
